export const getThemeById = (themeId) => {
  return VS_CODE_THEMES[themeId] || VS_CODE_THEMES.dark;
};

export const getDarkThemes = () => {
  return Object.entries(VS_CODE_THEMES)
    .filter(([_, theme]) => theme.mode === 'dark')
    .map(([id, theme]) => ({ id, ...theme }));
};

export const getLightThemes = () => {
  return Object.entries(VS_CODE_THEMES)
    .filter(([_, theme]) => theme.mode === 'light')
    .map(([id, theme]) => ({ id, ...theme }));
};

export const getDefaultThemeForMode = (mode) => {
  if (mode === 'dark') {
    return 'dark';
  }
  return 'light'; // Use default light theme
};

